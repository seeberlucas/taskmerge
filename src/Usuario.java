public class Usuario {
    private String nome;
    private int idade;
    private String email;
    private String username;
    private String senha;

    public Usuario(String nome, int idade, String email, String username, String senha) {
        this.nome = nome;
        this.idade = idade;
        this.email = email;
        this.username = username;
        this.senha = senha;
    }
}
